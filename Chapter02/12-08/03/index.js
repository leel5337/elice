// 비교 연산자

// 지시사항
//  1. 10과 10을 ==과 ===을 사용해 비교해 document.writeln()로 출력해보세요.
//  2. 10과 “10” 을 ==과 ===을 사용해 비교해 document.writeln()로 출력해보세요.
//  3. 10과 20을 !==을 사용해 비교해 document.writeln()로 출력해보세요.
//  4. 10과 20을 네 가지 부등호(>, >=, <, <=)를 사용해 비교해 document.writeln()로 출력해보세요.

document.write(10 == 10);
document.write(10 === 10);

document.write(10 == "10");
document.write(10 === "10");

document.writeln(10 !== 20);

document.writeln(10 > 20);
document.writeln(10 >= 20);
document.writeln(10 < 20);
document.writeln(10 <= 20);