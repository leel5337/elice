// abs를 사용하여 -3의 절댓값을 구하고 document.write()로 출력하세요.
// ceil를 사용하여 0.3의 올림 값을 구하고 document.write()로 출력하세요.
// floor를 사용하여 10.9의 내림 값을 구하고 document.write()로 출력하세요.
// random을 사용하여 임의의 숫자를 구하고 document.write()로 출력하세요.

