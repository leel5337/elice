// 변수 비교하기

// 지시사항
//  1. 변수 a를 선언하고 값을 10으로 설정합니다.
//  2. 변수 b를 선언하고 값을 10으로 설정합니다.
//  3. ==, ===을 사용해 a와 b를 비교해 웹 화면에 출력합니다.
//  4. 변수 b의 값을 문자열 “10”으로 변경합니다.
//  5. ==, ===을 사용해 a와 b를 비교해 웹 화면에 출력합니다.

let a = 10
let b = 10
document.write(a == b)
document.write(a === b)

b = '10'
document.write(a == b)
document.write(a === b)